package com.ariv.programiz.ds1;

public class TypesOfQueue {

	/**
	 * Types:
	 *  Simple Queue
	 *  Circular Queue
	 *  Priority Queue
	 *  Double Ended Queue(DeQue)
	 */
	
	/**
	 * Simple Queue:
	 *  In a simple queue, insertion takes place at the rear and removal 
	 *  occurs in the front. 
	 *  It strictly follows the FIFO (First In First Out)
	 *  
	 */
	
	/**
	 * Circular Queue:
	 *  In a Circular Queue the last element making point to the first element to make circular.
	 *  
	 *  Adv:
	 *   is main memory utilization
	 *   if the last position is full and the first position is empty we can insert in the front
	 */
	
	/**
	 * Priority Queue:
	 *  Elements are associated with priority and served according to its priority.
	 *  If elements with same priority occurs, they are served according to their order in the queue.
	 *  
	 */
	
	/**
	 * Deque (Double Ended Queue)
	 * 
	 *  insertion, deletion can be performed on both end, doesn't follow FIFO rule.
	 */
}
